<template>
    <nav class="bottom-pagination">
        <p-icon-button class="text"
                       name="ic_arrow_left"
                       :disabled="thisPage === 1"
                       @click="prevPage(thisPage)"
        />
        <div class="page-number">
            <div class="page-number-text">
                <span v-for="page in pageList.pages" :key="page"
                      @click="clickPage(page)"
                >
                    <span v-if="page === thisPage"><b>{{ thisPage }}</b></span>
                    <span v-else> {{ page }} </span>
                </span>
            </div>
        </div>

        <p-icon-button class="text"
                       name="ic_arrow_right"
                       :disabled="thisPage === pageList.totalPages"
                       @click="nextPage(thisPage)"
        />
    </nav>
</template>
<script lang="ts">
import PIconButton from '@/components/molecules/buttons/icon-button/PIconButton.vue';
import { computed, reactive, toRefs } from '@vue/composition-api';

export default {
    name: 'PBottomPagination',
    components: { PIconButton },
    props: {
        thisPage: {
            type: Number,
            validator(value) {
                return value > 0;
            },
        },
        pageStart: {
            type: Number,
            validator(value) {
                return value > 0;
            },
        },
        pageLimit: {
            type: Number,
            validator(value) {
                return value > 0;
            },
        },
        pageSize: {
            type: Number,
        },
        totalCount: {
            type: Number,
            required: true,
        },
    },
    setup(props, { emit }) {
        const paginate = (
            totalItems,
            currentPage,
            pageSize,
            maxPages,
        ) => {
            // calculate total pages
            const totalPages = Math.ceil(totalItems / pageSize);

            // ensure current page isn't out of range
            if (currentPage < 1) {
                // eslint-disable-next-line no-param-reassign
                currentPage = 1;
            } else if (currentPage > totalPages) {
                // eslint-disable-next-line no-param-reassign
                currentPage = totalPages;
            }

            let startPage: number; let endPage: number;
            if (totalPages <= maxPages) {
                // total pages less than max so show all pages
                startPage = 1;
                endPage = totalPages;
            } else {
                // total pages more than max so calculate start and end pages
                const maxPagesBeforeCurrentPage = Math.floor(maxPages / 2);
                const maxPagesAfterCurrentPage = Math.ceil(maxPages / 2) - 1;
                if (currentPage <= maxPagesBeforeCurrentPage) {
                    // current page near the start
                    startPage = 1;
                    endPage = maxPages;
                } else if (currentPage + maxPagesAfterCurrentPage >= totalPages) {
                    // current page near the end
                    startPage = totalPages - maxPages + 1;
                    endPage = totalPages;
                } else {
                    // current page somewhere in the middle
                    startPage = currentPage - maxPagesBeforeCurrentPage;
                    endPage = currentPage + maxPagesAfterCurrentPage;
                }
            }

            // calculate start and end item indexes
            const startIndex = (currentPage - 1) * pageSize;
            const endIndex = Math.min(startIndex + pageSize - 1, totalItems - 1);

            // create an array of pages to ng-repeat in the pager control
            const pages = Array.from(Array((endPage + 1) - startPage).keys()).map(i => startPage + i);

            // return object with all pager properties required by the view
            return {
                totalItems,
                currentPage,
                pageSize,
                totalPages,
                startPage,
                endPage,
                startIndex,
                endIndex,
                pages,
            };
        };

        const pageList = computed(() => paginate(props.totalCount, props.thisPage, props.pageSize, 10));

        const clickPage = (page) => {
            emit('clickPage', page);
        };

        const updatePage = (thisPage) => {
            emit('updatePage', thisPage);
        };

        const prevPage = (thisPage) => {
            emit('prevPage', thisPage);
        };

        const nextPage = (thisPage) => {
            emit('nextPage', thisPage);
        };

        return {
            paginate,
            pageList,
            clickPage,
            updatePage,
            prevPage,
            nextPage,
            iconHoverColor: 'transparent inherit',
        };
    },
};
</script>

